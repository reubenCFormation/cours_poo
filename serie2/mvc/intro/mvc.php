 Que est le MVC? Le M.V.C est une facon d'organiser son code et il est trés pratiqué dans le monde profresionelle. 
 Le M.V.C fonctionne de la maniere suivante.
 1) Nous avons tout d'abord un Router. Le router va inspecter les routes qui se declenchent.
 2) Lors que une route en particulier va se declencher, nous allons faire appel a une methode d'un Controller que nous aurai choisi (un Controller est une classe)
 3) Le Controller va lui meme souvent interroger un Model (aussi une classe). Les Models sont des classes qui mappe nos entités (nos tables dans une base de données).Ils vont faire des requettes a la base de données et ensuite vont retourner le resultat de la requette au controlleur
 4)Le Controlleur va ensuite recuperer ce que le Model lui a fourni et va ensuite transmettre cette information a une vue. La vue va afficher l'information.

 C'est un resumé trés simplistique mais c'est un peu l'idée de ce qui se passe
 


